use std::fs::{self, File, OpenOptions};
use std::io::Write;
use std::os::unix::fs::{OpenOptionsExt, PermissionsExt};
use std::path::Path;

use flagdeck_domain::{contract_schemas, typescript_declarations};

fn write_generated(path: &Path, contents: &[u8]) -> Result<(), Box<dyn std::error::Error>> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }
    let temporary = path.with_extension("tmp");
    let mut file = OpenOptions::new()
        .create(true)
        .truncate(true)
        .write(true)
        .mode(0o600)
        .open(&temporary)?;
    file.write_all(contents)?;
    file.sync_all()?;
    fs::rename(&temporary, path)?;
    fs::set_permissions(path, fs::Permissions::from_mode(0o644))?;
    File::open(path.parent().ok_or("generated path lacks parent")?)?.sync_all()?;
    Ok(())
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let root = Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(Path::parent)
        .ok_or("workspace root unavailable")?;
    let typescript_path = root.join("apps/desktop/src/generated/contracts.ts");
    let header = "// Generated by flagdeck-domain. Do not edit.\n\n";
    write_generated(
        &typescript_path,
        format!("{header}{}\n", typescript_declarations()).as_bytes(),
    )?;
    for (name, schema) in contract_schemas()? {
        let path = root.join(format!("schemas/{name}.schema.json"));
        write_generated(&path, &serde_json::to_vec_pretty(&schema)?)?;
    }
    Ok(())
}
